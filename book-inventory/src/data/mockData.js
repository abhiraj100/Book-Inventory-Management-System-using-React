// Book statistics helpers
export const getBookStats = (books) => {
  const totalBooks = books.length;
  const totalValue = books.reduce(
    (sum, book) => sum + book.price * book.stock,
    0
  );
  const totalStock = books.reduce((sum, book) => sum + book.stock, 0);
  const avgPrice =
    totalBooks > 0
      ? books.reduce((sum, book) => sum + book.price, 0) / totalBooks
      : 0;
  const genreDistribution = books.reduce((acc, book) => {
    acc[book.genre] = (acc[book.genre] || 0) + 1;
    return acc;
  }, {});

  return {
    totalBooks,
    totalValue,
    totalStock,
    avgPrice,
    genreDistribution,
  };
};

// Low stock alerts
export const getLowStockBooks = (books, threshold = 10) => {
  return books.filter((book) => book.stock <= threshold);
};

